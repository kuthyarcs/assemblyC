	TTL ASCENDING/DESCENDING
	AREA PROG,CODE,READONLY
START
	MOV R1,#4
	LDR R2,=Array
	LDR R3,=DST
UP
	LDR R4,[R2],#4
	STR R4,[R3],#4
	SUBS R1,R1,#1
	CMP R1,#0
	BNE UP
	MOV R7,#3
PASS
	MOV R1,#3
	LDR R0,=DST
COMP
	LDR R2,[R0],#4
	LDR R3,[R0]
	CMP R2,R3
	BLS CHECK
	STR R2,[R0],#-4
	STR R3,[R0]
	ADD R0,#4
CHECK
	SUBS R1,R1,#1
	CMP R1,#0
	BNE COMP
	SUBS R7,R7,#1
	CMP R7,#0
	BNE PASS
STOP
	B STOP
	
